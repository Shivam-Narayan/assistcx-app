// process and combine an array of data into a single formatted string
export const formatAndCombineValue = (
  data: { value?: string }[] = [],
) => {
  return data
    .map((item) => item?.value?.trim())// Safely trim and handle undefined values
    .filter((item) => item);
};

// This function is used to input text into an array of objects
export const formatStringArrayToObjectArray = (
  data: (string | { value?: string })[] = [],
) => {
  return Array.isArray(data)
    ? data.map((item) =>
        typeof item === "string" ? { value: item } : item,
      )
    : [];
};

// Parse text into an array of objects
export const parseTextToObjects = (inputText: string) =>
  inputText
    .split("\n")
    .map((line) => ({ value: line.trim() })) // Trim spaces and map to object
    .filter((item) => item.value); // Remove empty lines

/**
 * Converts a display name to a stable API identifier (snake_case): lowercases,
 * maps hyphens and whitespace to underscores, collapses runs of underscores.
 */
export const displayNameToIdentifierKey = (name: string) =>
  name
    .toLowerCase()
    .replace(/-/g, "_")
    .replace(/\s+/g, "_")
    .replace(/_+/g, "_");

/**
 * Same as displayNameToIdentifierKey, then strips any character that is not a–z, 0–9, or underscore.
 */
export const displayNameToStrictSnakeName = (name: string) =>
  displayNameToIdentifierKey(name).replace(/[^a-z0-9_]/g, "");

export const handleSpaceValidation = (event: any) => {
  if (event.key === " ") {
    const input = event.currentTarget;

    //  Prevent space at the beginning of the input
    if (input.selectionStart === 0) {
      event.preventDefault();
      return;
    }

    //Prevent multiple spaces between words
    const previousChar = input.value[input.selectionStart - 1];
    if (previousChar === " ") {
      event.preventDefault();
      return;
    }
  }
};


export interface ComboBoxItem {
  value: string;
  label: string;
  description?: string;
  [key: string]: any; // Allows for other properties
}

function isString(item: any): item is string {
  return typeof item === 'string';
}

export function formatComboBoxData(data: (string | ComboBoxItem)[] | undefined | null): ComboBoxItem[] {
  if (!data || data.length === 0) {
    return [];
  }

  if (isString(data[0])) {
    return (data as string[]).map(item => ({
      value: item,
      label: item,
    }));
  }

  return data as ComboBoxItem[];
}
